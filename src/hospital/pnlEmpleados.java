    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        boolean validar = validarCampos();
        if(validar == true){
            clasesHospital.Empleados ce = new clasesHospital.Empleados();
            ce.setCedula(txtCedula.getText());
            ce.setNombres(txtNombres.getText());
            ce.setDireccion(txtDireccion.getText());
            ce.setTelefono(txtTelefono.getText());
            ce.setEdad(txtEdad.getText());
            ce.setJornada(validarSeleccion());
            ce.setIngreso(txtIngreso.getText());
            ce.setSalida(txtSalida.getText());
            ce.setArea(jcbArea.getSelectedItem().toString());
            ce.setPuesto(jcbxPuesto.getSelectedItem().toString());
            ce.guardarEmpleados();
            limpiarCampos();
            
            ce.mostrarEmpleados(tblEmpleados);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
   
    private String validarSeleccion() {
        String seleccion = "";
        if (jckMatutino.isSelected()) {
            seleccion = "Matutino";
            return seleccion;
        }
        if (jckVespertino.isSelected()) {
            seleccion = "Vespertino";
            return seleccion;
        }
        if (jckNocturno.isSelected()) {
            seleccion = "Nocturno";
            return seleccion;
        }
        return seleccion;
    }
