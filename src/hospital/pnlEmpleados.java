    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        boolean validar = validarCampos();
        if(validar == true){
            clasesHospital.Empleados ce = new clasesHospital.Empleados();
            ce.setCedula(txtCedula.getText());
            ce.setNombres(txtNombres.getText());
            ce.setDireccion(txtDireccion.getText());
            ce.setTelefono(txtTelefono.getText());
            ce.setEdad(txtEdad.getText());
            ce.setJornada(validarSeleccion());
            ce.setIngreso(txtIngreso.getText());
            ce.setSalida(txtSalida.getText());
            ce.setArea(jcbArea.getSelectedItem().toString());
            ce.setPuesto(jcbxPuesto.getSelectedItem().toString());
            ce.guardarEmpleados();
            limpiarCampos();
            
            ce.mostrarEmpleados(tblEmpleados);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
   
    private String validarSeleccion() {
        String seleccion = "";
        if (jckMatutino.isSelected()) {
            seleccion = "Matutino";
            return seleccion;
        }
        if (jckVespertino.isSelected()) {
            seleccion = "Vespertino";
            return seleccion;
        }
        if (jckNocturno.isSelected()) {
            seleccion = "Nocturno";
            return seleccion;
        }
        return seleccion;
    }
     private void limpiarCampos() {
        txtCedula.setText("");
        txtNombres.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtEdad.setText("");
        txtIngreso.setText("");
        txtSalida.setText("");
        btngJornada.clearSelection();
        habilitarBoton();
        
        
    }//TODO -- limpiarCampos
     private boolean validarCampos() {
        if (txtCedula.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo cedula");
            return false;
        }else if(txtNombres.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo nombre");
            return false;
        }else if(txtDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo direccci√≥n");
            return false;
        }else if(txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo telefono");
            return false;
        }else if(txtEdad.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo edad");
            return false;
        }else if(txtIngreso.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo ingreso");
            return false;
        }else if(txtSalida.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo salida");
            return false;
        }
        return  true;
    }//TODO -- validarCampos
