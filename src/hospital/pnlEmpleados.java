    public void habilitarBoton() {
        if (!txtCedula.getText().isEmpty()
              && !txtNombres.getText().isEmpty()  
              && !txtDireccion.getText().isEmpty()
              && !txtTelefono.getText().isEmpty()
              && !txtEdad.getText().isEmpty()
              && !txtIngreso.getText().isEmpty()
              && !txtSalida.getText().isEmpty()
              ) {
            jcbxPuesto.setEnabled(true);
            jcbArea.setEnabled(true);
            jckMatutino.setEnabled(true);
            jckVespertino.setEnabled(true);
            jckNocturno.setEnabled(true);
            btnGuardar.setEnabled(true);
        }else {
            jcbxPuesto.setEnabled(false);
            jcbxPuesto.setSelectedIndex(0);
            jcbArea.setEnabled(false);
            jcbArea.setSelectedIndex(0);
            jckMatutino.setEnabled(false);
            jckMatutino.setSelected(false);
            jckVespertino.setEnabled(false);
            jckVespertino.setSelected(false);
            jckNocturno.setEnabled(false);
            jckNocturno.setSelected(false);
            btnGuardar.setEnabled(false);
        }
    }
