    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
         String fecha;
        SimpleDateFormat f = new SimpleDateFormat("yyyy/MM/dd");
        //pociciono en la variable
        fecha = f.format(jdcFechaVenta.getDate());
        boolean validar = validarCampos();
        if(validar == true){
            clasesHospital.Ventas cv = new clasesHospital.Ventas();
            cv.setMedicamento(txtMedicamento.getText());
            cv.setCantidad(txtCantidad.getText());
            cv.setSubtotal(txtSubtotal.getText());
            cv.setRemitente(txtRemitente.getText());
            cv.setTipo(validarSeleccion());
            cv.setFechaVenta(fecha);
            cv.setCodigo(txtCodigo.getText());
            cv.setTotal(txtTotal.getText());
            cv.guardarVentas();
            limpiarCampos();
            
            cv.mostrarVentas(tblVentas);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
   
    private String validarSeleccion() {
        String seleccion = "";
        if (jckJarabe.isSelected()) {
            seleccion = "Jarabe";
            return seleccion;
        }
        if (jckTableta.isSelected()) {
            seleccion = "Tableta";
            return seleccion;
        }
        if (jckInyectable.isSelected()) {
            seleccion = "Inyectable";
            return seleccion;
        }
        if (jckViaOral.isSelected()) {
            seleccion = "Via oral";
            return seleccion;
        }
        return seleccion;
    }
     private void limpiarCampos() {
        txtMedicamento.setText("");
        txtCantidad.setText("");
        txtSubtotal.setText("");
        txtRemitente.setText("");
        txtCodigo.setText("");
        txtTotal.setText("");
        btngTipoMedicamento.clearSelection();
        habilitarBoton();
    }//TODO -- limpiarCampos
     private boolean validarCampos() {
        if (txtMedicamento.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo cedula");
            return false;
        }else if(txtCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo nombre");
            return false;
        }else if(txtSubtotal.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo direccci√≥n");
            return false;
        }else if(txtRemitente.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo telefono");
            return false;
        }else if(txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo estado paciente");
            return false;
        }else if(txtTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Complete el campo estado paciente");
            return false;
        }
        return  true;
    }//TODO -- validarCampos
