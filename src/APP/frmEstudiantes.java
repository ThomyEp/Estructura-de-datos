    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String cedula=txtCedula.getText();
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        int edad=Integer.parseInt(txtEdad.getText());
        String sexo;
        if(rbMasculino.isSelected()){
            sexo = "M";
        }else if(rbFemenino.isSelected()){
            sexo = "F";
        }else{
            sexo = "M";
        }
        String telefono=txtTelefono.getText();
        
        try{
        String sentencia="INSERT INTO Estudiante(ced_est, nom_est, ape_est, eda_est, sex_est, tel_est, est_est) VALUES(?,?,?,?,?,?,?)";    
        Connection con=Conexion.getConnection();
        PreparedStatement ps=con.prepareStatement(sentencia);
        ps.setString(1, cedula);
        ps.setString(2, nombre);
        ps.setString(3, apellido);
        ps.setInt(4, edad);
        ps.setString(5, sexo);
        ps.setString(6, telefono);
        ps.setString(7, "Activo");
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Se guardo el estudiante correctamente ");
        limpiar();
        cargarTabla();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No se guardo el estudiante correctamente " +e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

