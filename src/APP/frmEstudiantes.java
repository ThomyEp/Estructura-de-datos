    private void tblEstudiantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEstudiantesMouseClicked
        // TODO add your handling code here:
        try {
            int fila = tblEstudiantes.getSelectedRow();
            int id = Integer.parseInt(tblEstudiantes.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            
            Connection con = Conexion.getConnection();
            ps = con.prepareStatement("SELECT  ced_est, nom_est, ape_est, eda_est, sex_est, tel_est, est_est FROM Estudiante WHERE id_est=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while (rs.next()) {                
               txtId.setText(String.valueOf(id));
               txtCedula.setText(rs.getString("ced_est"));
               txtCedula.setEnabled(false);
               txtNombre.setText(rs.getString("nom_est"));
               txtApellido.setText(rs.getString("ape_est"));
               txtEdad.setText(rs.getString("eda_est"));
               txtTelefono.setText(rs.getString("tel_est"));
                if (rs.getString("sex_est").equals("M")) {
                    rbMasculino.setSelected(true);
                }else if (rs.getString("sex_est").equals("F")) {
                    rbFemenino.setSelected(true);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblEstudiantesMouseClicked

